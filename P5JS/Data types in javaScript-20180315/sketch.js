// STEP 1: Variables
// All variables in js are defined with the word var.
// Types like String, int, float, boolean, etc don't need to be defined
var name = "John Doe";		// in java this would be String name
var age = 57;				// in java this would be int age
var height = 1.81;			// in java this would be float height
var intelligent = false;	// in java this would be boolean intelligent

// print variables to console
console.log(name);
console.log(age);
console.log(height);
console.log(intelligent);



